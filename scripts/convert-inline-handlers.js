/**
 * convert-inline-handlers.js — Mechanically rewrite simple inline event
 * attributes to `data-pfc-*` attributes + an addEventListener bootstrap
 * appended to each file's <script> tail.
 *
 * Patterns converted (no semantic change — same behavior, no inline JS):
 *
 *   onclick="fnName()"               -> data-pfc-on-click="fnName"
 *   onclick="fnName('lit')"          -> data-pfc-on-click="fnName"
 *                                       data-pfc-arg='"lit"'
 *   onclick="fnName(this)"           -> data-pfc-on-click="fnName"
 *                                       data-pfc-arg-this
 *   oninput="fnName(this.value)"     -> data-pfc-on-input="fnName"
 *                                       data-pfc-arg-this-value
 *   onclick="window.location.href='/x'" -> data-pfc-on-click="navigate"
 *                                          data-pfc-href="/x"
 *   on*="document.getElementById('X').textContent=this.value"
 *                                    -> data-pfc-on-input="setText"
 *                                       data-pfc-target="X"
 *
 * After the file is processed, a small bootstrap script is appended that
 * walks the DOM for these attrs and wires real addEventListener calls.
 * The bootstrap reads the global function by name (`window[fnName]`)
 * which preserves the existing call semantics — all the page-specific
 * functions (handleLogin, recalcForecast, setStrategy, etc.) remain
 * defined exactly where they were.
 *
 * Skipped: any handler we can't classify cleanly. The script reports
 * the count + first 5 examples so we know what's left for manual surgery.
 *
 * Run:  node scripts/convert-inline-handlers.js
 */
'use strict';
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const EVENTS = ['click', 'input', 'change', 'submit', 'keyup', 'keydown', 'keypress',
                'focus', 'blur', 'mouseover', 'mouseout', 'mouseenter', 'mouseleave',
                'wheel'];

const BOOTSTRAP_MARKER = 'PFC_INLINE_BOOTSTRAP_v3';
const BOOTSTRAP = `<script>
/* ${BOOTSTRAP_MARKER} — wires data-pfc-on-* attributes to addEventListener.
   Generated by scripts/convert-inline-handlers.js. Do not edit by hand. */
(function(){'use strict';
  function callFn(name, args){
    var fn = window[name];
    if (typeof fn === 'function') {
      try { return fn.apply(null, args); }
      catch(e){ console.error('[pfc-inline] '+name+': '+e.message); }
    } else if (name.charAt(0) !== '_') {
      console.warn('[pfc-inline] handler not defined: '+name);
    }
  }
  // Internal handlers — prefixed _pfc_ so they don't collide with page fns.
  var INTERNAL = {
    _pfc_nav: function(el){ window.location.href = el.getAttribute('data-pfc-href'); },
    _pfc_settext: function(el){
      var tgt = document.getElementById(el.getAttribute('data-pfc-target'));
      if (tgt) tgt.textContent = el.value;
    },
    _pfc_trigger_click: function(el){
      var tgt = document.getElementById(el.getAttribute('data-pfc-target'));
      if (tgt) tgt.click();
    },
    _pfc_style: function(el){
      el.style[el.getAttribute('data-pfc-style-prop')] = el.getAttribute('data-pfc-style-value');
    },
    _pfc_style2: function(el){
      try {
        var spec = JSON.parse(el.getAttribute('data-pfc-style'));
        for (var k in spec) el.style[k] = spec[k];
      } catch(_){}
    }
  };
  var EV = ${JSON.stringify(EVENTS)};
  EV.forEach(function(ev){
    var attr = 'data-pfc-on-' + ev;
    document.querySelectorAll('['+attr+']').forEach(function(el){
      el.addEventListener(ev, function(e){
        var fn = el.getAttribute(attr);
        // Key-guarded handlers — only fire if event.key matches
        var requiredKey = el.getAttribute('data-pfc-key');
        if (requiredKey && e.key !== requiredKey) return;
        if (INTERNAL[fn]) return INTERNAL[fn](el);
        // Build args list. Order rules:
        //   - data-pfc-arg-this           => el goes FIRST
        //   - data-pfc-arg-this-value     => el.value goes FIRST
        //   - data-pfc-arg (JSON; if array, splat) appends
        //   - data-pfc-arg-this-trailing  => el goes LAST (after data-pfc-arg)
        var args = [];
        if (el.hasAttribute('data-pfc-arg-event')) args.push(e);
        else if (el.hasAttribute('data-pfc-arg-this')) args.push(el);
        else if (el.hasAttribute('data-pfc-arg-this-value')) args.push(el.value);
        if (el.hasAttribute('data-pfc-arg')) {
          var raw = el.getAttribute('data-pfc-arg');
          try {
            var parsed = JSON.parse(raw);
            if (Array.isArray(parsed) && !el.hasAttribute('data-pfc-arg-this')) {
              args = args.concat(parsed);
            } else {
              args.push(parsed);
            }
          } catch(_) { args.push(raw); }
        }
        if (el.hasAttribute('data-pfc-arg-this-trailing')) args.push(el);
        var rv = callFn(fn, args);
        // Chained side-effect calls (data-pfc-then="fn1,fn2") run after the main
        // call. These take no args; they're for the multi-statement pattern
        // updateSlider(args); recalcForecast(); refreshInflBoxes().
        var then = el.getAttribute('data-pfc-then');
        if (then) then.split(',').forEach(function(name){ name = name.trim(); if (name) callFn(name, []); });
        return rv;
      });
    });
  });
})();
</script>`;

function walk(dir, out = []) {
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    if (e.isDirectory()) {
      if (['node_modules', '.git', 'docs', 'assets', 'css', 'js', 'api', '.github', 'scripts'].includes(e.name)) continue;
      walk(path.join(dir, e.name), out);
    } else if (e.name.endsWith('.html')) {
      out.push(path.join(dir, e.name));
    }
  }
  return out;
}

const RE_HANDLER = new RegExp(`\\b(on(?:${EVENTS.join('|')}))="([^"]+)"`, 'g');

function tryConvert(event, expr) {
  const e = expr.trim().replace(/\s+/g, ' ').replace(/;\s*$/, '');
  // 1. fnName()
  let m = /^([a-zA-Z_$][\w$]*)\(\)$/.exec(e);
  if (m) return ` data-pfc-on-${event}="${m[1]}"`;
  // 2a. fnName("literal-string")
  m = /^([a-zA-Z_$][\w$]*)\(\s*(['"])([^'"]*)\2\s*\)$/.exec(e);
  if (m) return ` data-pfc-on-${event}="${m[1]}" data-pfc-arg='${JSON.stringify(m[3])}'`;
  // 2b. fnName(42)
  m = /^([a-zA-Z_$][\w$]*)\(\s*(-?\d+(?:\.\d+)?)\s*\)$/.exec(e);
  if (m) return ` data-pfc-on-${event}="${m[1]}" data-pfc-arg='${m[2]}'`;
  // 3. fnName(this)
  m = /^([a-zA-Z_$][\w$]*)\(\s*this\s*\)$/.exec(e);
  if (m) return ` data-pfc-on-${event}="${m[1]}" data-pfc-arg-this`;
  // 3b. fnName(this, literal)  -> el first, then literal (string OR number)
  m = /^([a-zA-Z_$][\w$]*)\(\s*this\s*,\s*((?:['"][^'"]*['"]|-?\d+(?:\.\d+)?))\s*\)$/.exec(e);
  if (m) {
    const v = m[2].startsWith('"') || m[2].startsWith("'") ? m[2].slice(1, -1) : Number(m[2]);
    return ` data-pfc-on-${event}="${m[1]}" data-pfc-arg-this data-pfc-arg='${JSON.stringify(v)}'`;
  }
  // 3c. fnName(literal, this)  -> reverse: literal first, then el
  m = /^([a-zA-Z_$][\w$]*)\(\s*((?:['"][^'"]*['"]|-?\d+(?:\.\d+)?))\s*,\s*this\s*\)$/.exec(e);
  if (m) {
    const v = m[2].startsWith('"') || m[2].startsWith("'") ? m[2].slice(1, -1) : Number(m[2]);
    return ` data-pfc-on-${event}="${m[1]}" data-pfc-arg='${JSON.stringify(v)}' data-pfc-arg-this-trailing`;
  }
  // 3d. fnName(event)  -> el goes via event.target; just pass event-shim
  m = /^([a-zA-Z_$][\w$]*)\(\s*event\s*\)$/.exec(e);
  if (m) return ` data-pfc-on-${event}="${m[1]}" data-pfc-arg-event`;
  // 3e. if(event.key==='X')fnName()  -> key-guarded no-arg call
  m = /^if\(\s*event\.key\s*===\s*(['"])([^'"]+)\1\s*\)\s*([a-zA-Z_$][\w$]*)\(\)$/.exec(e);
  if (m) return ` data-pfc-on-${event}="${m[3]}" data-pfc-key="${m[2]}"`;
  // 4. fnName(this.value)
  m = /^([a-zA-Z_$][\w$]*)\(\s*this\.value\s*\)$/.exec(e);
  if (m) return ` data-pfc-on-${event}="${m[1]}" data-pfc-arg-this-value`;
  // 5. window.location.href = '/x'
  m = /^window\.location\.href\s*=\s*(['"])([^'"]*)\1$/.exec(e);
  if (m) return ` data-pfc-on-${event}="_pfc_nav" data-pfc-href="${m[2]}"`;
  // 6. document.getElementById('X').textContent = this.value  (slider display)
  m = /^document\.getElementById\(\s*(['"])([^'"]+)\1\s*\)\.textContent\s*=\s*this\.value$/.exec(e);
  if (m) return ` data-pfc-on-${event}="_pfc_settext" data-pfc-target="${m[2]}"`;
  // 7. document.getElementById('X').click()  (trigger another element)
  m = /^document\.getElementById\(\s*(['"])([^'"]+)\1\s*\)\.click\(\)$/.exec(e);
  if (m) return ` data-pfc-on-${event}="_pfc_trigger_click" data-pfc-target="${m[2]}"`;
  // 8. fnName(N, M, ...)  - 2-4 literal args (strings, numbers, booleans)
  //    e.g. openCheckout('founders', 149)
  //         updateSlider('sl-x','sv-x','%')
  //         updateSlider('sl-x','sv-x','%',true)
  //         updateSlider('sl-x','sv-x','$',false,true)
  m = /^([a-zA-Z_$][\w$]*)\(\s*((?:['"][^'"]*['"]|-?\d+(?:\.\d+)?|true|false)(?:\s*,\s*(?:['"][^'"]*['"]|-?\d+(?:\.\d+)?|true|false)){1,5})\s*\)$/.exec(e);
  if (m) {
    const parts = m[2].split(/\s*,\s*/);
    const args = parts.map(p => {
      if (p === 'true') return true;
      if (p === 'false') return false;
      if (p.startsWith('"') || p.startsWith("'")) return p.slice(1, -1);
      return Number(p);
    });
    return ` data-pfc-on-${event}="${m[1]}" data-pfc-arg='${JSON.stringify(args)}'`;
  }
  // 8b. fn(args); fn2(); fn3()  - multi-statement where first call is the
  //     parameterized one and the rest are no-arg side-effect calls
  m = /^([a-zA-Z_$][\w$]*)\(\s*((?:['"][^'"]*['"]|-?\d+(?:\.\d+)?|true|false)(?:\s*,\s*(?:['"][^'"]*['"]|-?\d+(?:\.\d+)?|true|false)){0,5})\s*\)\s*;\s*(.+)$/.exec(e);
  if (m) {
    // Validate that the remaining statements are all simple fnName() no-arg calls.
    const tailFns = [];
    const rest = m[3].split(';').map(s => s.trim()).filter(Boolean);
    let ok = true;
    for (const stmt of rest) {
      const fm = /^([a-zA-Z_$][\w$]*)\(\s*\)$/.exec(stmt);
      if (!fm) { ok = false; break; }
      tailFns.push(fm[1]);
    }
    if (ok) {
      const parts = m[2].split(/\s*,\s*/);
      const args = parts.map(p => {
        if (p === 'true') return true;
        if (p === 'false') return false;
        if (p.startsWith('"') || p.startsWith("'")) return p.slice(1, -1);
        return Number(p);
      });
      return ` data-pfc-on-${event}="${m[1]}" data-pfc-arg='${JSON.stringify(args)}' data-pfc-then="${tailFns.join(',')}"`;
    }
  }
  // 9. this.style.PROP = 'VALUE'  (single-prop hover style)
  m = /^this\.style\.([a-zA-Z]+)\s*=\s*(['"])([^'"]*)\2$/.exec(e);
  if (m) return ` data-pfc-on-${event}="_pfc_style" data-pfc-style-prop="${m[1]}" data-pfc-style-value="${m[3]}"`;
  // 10. this.style.A='X';this.style.B='Y'  (two-prop hover style)
  m = /^this\.style\.([a-zA-Z]+)\s*=\s*(['"])([^'"]*)\2\s*;\s*this\.style\.([a-zA-Z]+)\s*=\s*(['"])([^'"]*)\5$/.exec(e);
  if (m) return ` data-pfc-on-${event}="_pfc_style2" data-pfc-style='${JSON.stringify({[m[1]]: m[3], [m[4]]: m[6]})}'`;
  return null;
}

let total = 0;
let converted = 0;
const skippedExamples = [];
const fileStats = {};

const files = walk(ROOT);
for (const file of files) {
  const orig = fs.readFileSync(file, 'utf8');
  let src = orig;
  let fileConverted = 0;
  let fileSkipped = 0;
  // Use replace with a callback so we transform in-place.
  src = src.replace(RE_HANDLER, (match, attrName, expr) => {
    total++;
    const ev = attrName.slice(2); // strip "on"
    const replacement = tryConvert(ev, expr);
    if (replacement) {
      converted++; fileConverted++;
      return replacement.trimStart() ? ' ' + replacement.trimStart() : replacement;
    }
    fileSkipped++;
    if (skippedExamples.length < 8) {
      skippedExamples.push({ file: path.relative(ROOT, file), attr: attrName, expr: expr.slice(0, 100) });
    }
    return match; // leave inline handler untouched
  });

  // If any handlers were converted, append the bootstrap (unless it's already present).
  if (fileConverted > 0 && !src.includes(BOOTSTRAP_MARKER)) {
    // Insert bootstrap just before </body>. Fall back to end of file if no </body>.
    const m = src.match(/<\/body>/i);
    if (m) {
      src = src.slice(0, m.index) + BOOTSTRAP + '\n' + src.slice(m.index);
    } else {
      src += '\n' + BOOTSTRAP + '\n';
    }
  }

  if (src !== orig) {
    fs.writeFileSync(file, src, 'utf8');
    fileStats[path.relative(ROOT, file).replace(/\\/g, '/')] =
      { converted: fileConverted, skipped: fileSkipped };
  }
}

console.log(`=== Conversion summary ===`);
console.log(`Total handlers found:      ${total}`);
console.log(`Mechanically converted:    ${converted}  (${((converted/total)*100).toFixed(1)}%)`);
console.log(`Skipped (complex, manual): ${total - converted}  (${(((total-converted)/total)*100).toFixed(1)}%)`);
console.log();
console.log(`Files modified: ${Object.keys(fileStats).length}`);
console.log();
console.log(`=== Files with skipped handlers (need manual surgery) ===`);
const filesWithSkipped = Object.entries(fileStats).filter(([, s]) => s.skipped > 0)
  .sort((a, b) => b[1].skipped - a[1].skipped);
for (const [f, s] of filesWithSkipped) {
  console.log(`  ${String(s.skipped).padStart(3)}  ${f}  (also converted: ${s.converted})`);
}
console.log();
console.log(`=== Sample skipped expressions (first 8) ===`);
for (const ex of skippedExamples) {
  console.log(`  ${ex.file}  ${ex.attr}="${ex.expr}"`);
}
